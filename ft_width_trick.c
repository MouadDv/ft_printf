#include "ft_printf.h"

void	ft_width_trick(int len, char c, int *i)
{
	int j;

	j = 0;
	if (len > 0)
	{
		while (j < len)
		{
			*i = *i + 1;
			write(1, &c, 1);
			j++;
		}
	}
}
