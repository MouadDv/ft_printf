#include "ft_printf.h"

void	ft_putunsigned(unsigned int nb, int *i)
{
	char	k;

	if (nb >= 10)
	{
		*i = *i + 1;
		ft_putunsigned(nb / 10, i);
		k = nb % 10 + 48;
		write(1, &k, 1);
	}
	else
	{
		*i = *i + 1;
		k = nb + 48;
		write(1, &k, 1);
	}
}
